package tarea1;

public class Ejercicio6 {
    public static void main(String[] args) {
        int numero = 10;
        if (numero == 1)
            System.out.println("Uno");
        else if (numero == 2)
            System.out.println("Dos");
        else if (numero == 3)
            System.out.println("Tres");
        else
            System.out.println("Ni uno, ni dos, ni tres");
    }
}
